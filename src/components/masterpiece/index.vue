<script lang="ts" setup>
type MasterpieceProps = {
  /**
   * The masterpiece index.
   */
  index: number;

  /**
   * The masterpiece image.
   */
  src: string;

  /**
   * Mastrerspiece array.
   */
  masterpiece: string[];
}

/**
 * Defines the props for the masterpiece component.
 */
const props = defineProps<MasterpieceProps>();

/**
 * Track download state to provide feedback
 */
const isDownloading = ref(false);
const downloadSuccess = ref(false);

/**
 * Removes the masterpiece from the state when it fails to load.
 */
const onImageError = (index: number) => {
  if (props.masterpiece) props.masterpiece.splice(index, 1);
};

/**
 * Downloads the current image as a PNG file.
 */
const downloadImage = async () => {
  if (isDownloading.value) return;
  
  isDownloading.value = true;
  downloadSuccess.value = false;


  try {
    const response = await fetch(`/api/image?url=${encodeURIComponent(props.src)}`);

    if (!response.ok) {
      throw new Error(`Failed to fetch image, Status: ${response.status}`);
    }

    const contentType = response.headers.get('Content-Type');

    if (!contentType || !contentType.includes('image/png')) {
      throw new Error('The server did not return a valid PNG image');
    }

    const blob = await response.blob();
    const url = window.URL.createObjectURL(blob);

    const a = document.createElement('a');
    a.href = url;
    a.download = `masterpiece_${Date.now()}.png`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    window.URL.revokeObjectURL(url);
  } catch (error) {
    console.error('Failed to download image:', error);
  } finally {
    isDownloading.value = false;
  }
};
</script>

<template>
  <div class="relative">
    <img :src="props.src" alt="Masterpiece Image" class="w-full h-auto" @error="onImageError(props.index)" />
    
    <button @click.stop="downloadImage"
      class="absolute bottom-2 right-2 bg-black bg-opacity-50 text-white p-2 rounded-full hover:bg-opacity-70 focus:outline-none"
      :class="{ 'opacity-50 cursor-wait': isDownloading, 'bg-green-500 bg-opacity-70': downloadSuccess }"
      :disabled="isDownloading"
      title="Save as PNG">
      
      <!-- Loading spinner when downloading -->
      <svg v-if="isDownloading" class="animate-spin h-5 w-5" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
        <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4"></circle>
        <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
      </svg>
      
      <!-- Success icon when download completed -->
      <svg v-else-if="downloadSuccess" xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
        <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M5 13l4 4L19 7" />
      </svg>
      
      <!-- Default download icon -->
      <svg v-else xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
        <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2"
          d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-4l-4 4m0 0l-4-4m4 4V4" />
      </svg>
    </button>
  </div>
</template>